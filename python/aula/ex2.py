x = int(input("Insira o valor de X: "))

if x > 0 :
    print("O 'X' é positivo")
if x == 1 or x == 5 :
    print("O 'X' é 1 ou 5")
if x >= 20 and x <= 25 :
    print("O 'X' está entre 20 e 25")



if x % 2 == 0 :
    print("O 'X' é divisível por 2")
elif x % 3 == 0 :
    print("O 'X' é divisível por 3")
elif x % 5 == 0 :
    print("O 'X' é divisível por 5")

